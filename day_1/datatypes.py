#1. Data Types
x = 10 #int - Signed Inters
y = 3.14 #float
z = 2 + 3j #complex
name = "John" #string
nameb = b"John" #bytes

print(type(x))
print(type(y))
print(type(z))
print(type(name))
print(type(nameb))


#2. Casting
x = str(3)    # x will be '3'
y = int(3)    # y will be 3
z = float(3)  # z will be 3.0

print(type(x))
print(type(y))
print(type(z))


