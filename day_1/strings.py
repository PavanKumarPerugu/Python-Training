#2. Slicing Strings
b = "Hello, World!"
print(b[2:5])

#3. Negative Indexing
b = "Hello, World!"
print(b[-5:-2])


#4. Modify Strings
a = "Hello, World!"
print(a.upper())

a = " Hello, World! "
print(a.strip()) # returns "Hello, World!"

